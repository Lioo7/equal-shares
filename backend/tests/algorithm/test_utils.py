from src.algorithm.utils import find_max


def test_find_max_passed() -> None:
    bids = {
        11: {1: 100, 2: 100, 4: 100},
        12: {2: 150, 5: 150},
        13: {1: 200, 5: 200},
        14: {3: 250, 4: 250},
        15: {2: 300, 3: 300, 5: 300},
        16: {2: 350, 5: 350},
        17: {1: 400, 4: 400},
        18: {2: 450, 5: 450},
        19: {1: 500, 3: 500, 5: 500},
        20: {2: 550, 3: 550},
    }

    actual = find_max(bids)

    expected = {11: 100, 12: 150, 13: 200, 14: 250, 15: 300, 16: 350, 17: 400, 18: 450, 19: 500, 20: 550}

    assert actual == expected
